        //                     3322222 22222 11111 111 11000 0000000
        //                     1098765 43210 98765 432 10987 6543210
        // aes_enc_round:      ssss000 bbbbb aaaaa 000 ddddd 0001011
        // aes_enc_round_last: ssss010 bbbbb aaaaa 000 ddddd 0001011
        // aes_dec_round:      ssss001 bbbbb aaaaa 000 ddddd 0001011
        // aes_dec_round_last: ssss011 bbbbb aaaaa 000 ddddd 0001011
        public void HandleAesInstruction(UInt64 opcode)
        {
            var rd = (int)BitHelper.GetValue(opcode, 7, 5);
            var rs1 = (int)BitHelper.GetValue(opcode, 15, 5);
            var rs2 = (int)BitHelper.GetValue(opcode, 20, 5);
            var is_dec = BitHelper.IsBitSet(opcode, 25);
            var is_last = BitHelper.IsBitSet(opcode, 26);
            var byte_sel = (int)BitHelper.GetValue(opcode, 28, 2);


            var arg1 = (UInt32)GetRegisterUnsafe(rs1).RawValue;
            var arg2 = (UInt32)GetRegisterUnsafe(rs2).RawValue;
            var byte_val = (arg2 >> (byte_sel * 8)) & 0xff;

            UInt32 result = 0;
            if (is_dec)
            {
                // print("Handling%s AES DEcode, instruction is %08x (%s), rs1: x%d (%08x), rs2: x%d (%08x), rd: x%d" %
                //       (is_last_str, opcode, bin(opcode), rs1, arg1, rs2, arg2, rd))
                if (is_last)
                {
                    result = arg1 ^ decrypt_last_ref[byte_sel][byte_val];
                }
                else
                {
                    result = arg1 ^ decrypt_ref[byte_sel][byte_val];
                }
            }
            else
            {
                // print("Handling%s AES enCODE, instruction is %08x (%s), rs1: x%d (%08x), rs2: x%d (%08x), rd: x%d, byte#: %d (%02x)" %
                //       (is_last_str, opcode, bin(opcode), rs1, arg1, rs2, arg2, rd, byte_sel, byte_val))
                if (is_last)
                {
                    result = arg1 ^ encrypt_last_ref[byte_sel][byte_val];
                }
                else
                {
                    result = arg1 ^ encrypt_ref[byte_sel][byte_val];
                }
            }

            // this.Log(LogLevel.Noisy, "PC@0x{8:X}: Handling aes instruction: is dec? {0}, is last? {1}, rs1: x{2} ({5:X8}), rs2: x{3} ({6:X8}), written to x{4} ({7:X8})",
            //             is_dec, is_last, rs1, rs2, rd, arg1, arg2, result, PC.RawValue);

            SetRegisterUnsafe(rd, (ulong)result);
        }

